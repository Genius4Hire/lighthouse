const without = function(arr1, itemsToRemove) {
  let newArray = [];
  for (let item of arr1) {
    if (!itemsToRemove.includes(item)) {
      newArray.push(item);
    }
  }
  assertArraysEqual(newArray, eqArrays(arr1, newArray));
  return newArray;
};

